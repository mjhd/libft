int		ft_digits(int n)
{
	int		len;
	long	n_cpy;

	len = 0;
	n_cpy = (long)n;
	if (n_cpy <= 0)
	{
		len++;
		n_cpy = -n_cpy;
	}
	while (n_cpy != 0)
	{
		len++;
		n_cpy /= 10;
	}
	return (len);
}
