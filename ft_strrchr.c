#include "libft.h"

char		*ft_strrchr(const char *s, int c)
{
	char	*val;
	char	test;
	char 	*pnt;

	val = (char *)s;
	test = c;

	while ((*val && test && test != *val++) || (!c && !(*val)))
		pnt = val;
	return (test == *val) ? &(*val) : 0;
}