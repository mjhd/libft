#include "libft.h"

char    *ft_strrchr(const char *s, int c)
{
	int length;

	length = 0;
	while (s && *s && ++length)
		s = s + 1;
	while ((*s != c || c == '\0') && length--)
	{
		if (*s == c)
			return((char *)s);
		(char *)s--;
	}
	return(0);
}
