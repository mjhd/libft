#include "libft.h"

char    *ft_strrchr(const char *s, int c)
{
    int length;

    length = 0;
    while (s && *s && ++length)
        s = s + 1;
    while ((c == '\0' || *(s - 1)) && length--)
    {
        if (*s-- == c)
            return((char *)++s);
    }
    return(0);
}
