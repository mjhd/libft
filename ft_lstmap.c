#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*newlist;

	newlist = ft_lstnew((*lst).content, (*lst).content_size);
	while ((*lst).next)
	{
		lst = (*lst).next;
		ft_lstadd(&newlist, f(lst));
	}
	return (newlist);
}
