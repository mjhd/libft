#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t size)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && i++ < (int)size)
	{
		if (s1[i - 1] != s2[i - 1])
			return (0);
	}
	return (1);
}
