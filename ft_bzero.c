#include "libft.h"

void	ft_bzero(void *dst, size_t size)
{
	unsigned char	*wrt_pnt;
	int				i;

	wrt_pnt = dst;
	i = 0;
	while (i < (int)size)
		wrt_pnt[i++] = (unsigned char)'0';
}
